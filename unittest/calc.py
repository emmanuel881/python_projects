def add(x, y):
    """
    :param x: this is the first number 
    :param y: this is the second number
    :return: addition of x and y
    """
    return x + y

def sub(x, y):
    """
    :param x: this is the first number 
    :param y: this is the second number
    :return: subtraction of x and y
    """
    return x - y

def division(x, y):
    """
    :param x: this is the first number 
    :param y: this is the second number
    :return: division of x and y
    """
    return x / y

def mult(x, y):
    """
    :param x: this is the first number 
    :param y: this is the second number
    :return: multliplication of x and y
    """
    return x * y

def intDiv(x, y):
    """
    :param x: this is the first number 
    :param y: this is the second number
    :return: integer division of x and y
    """
    return x + y
